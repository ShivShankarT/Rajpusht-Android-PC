package in.rajpusht.pc.model;

public class ChildModel {
    private String dob;
    private int age;
    private String stage;
    private String sub_stage;
    private int mother_id;
    private String delivery_home;
    private String delivery_place;
    private int child_order;

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getStage() {
        return stage;
    }

    public void setStage(String stage) {
        this.stage = stage;
    }

    public String getSub_stage() {
        return sub_stage;
    }

    public void setSub_stage(String sub_stage) {
        this.sub_stage = sub_stage;
    }

    public int getMother_id() {
        return mother_id;
    }

    public void setMother_id(int mother_id) {
        this.mother_id = mother_id;
    }

    public String getDelivery_home() {
        return delivery_home;
    }

    public void setDelivery_home(String delivery_home) {
        this.delivery_home = delivery_home;
    }

    public String getDelivery_place() {
        return delivery_place;
    }

    public void setDelivery_place(String delivery_place) {
        this.delivery_place = delivery_place;
    }

    public int getChild_order() {
        return child_order;
    }

    public void setChild_order(int child_order) {
        this.child_order = child_order;
    }
}
